package com.igorcrevar.rolloversphere.objects.boxes;

public enum BoxType {
	NOT_UPGRADE,
	UPGRADE_EASY_CHANGE_DIRECTION,
	UPGRADE_SPEED;
	
	public String getText(){
		switch (this){
		case UPGRADE_EASY_CHANGE_DIRECTION:
			return "UPGRADE FAST DIRECTION CHANGE!";
		case UPGRADE_SPEED:
			return "UPGRADE SPEED!";
		}
		
		return null;
	}
}
